#include<bits/stdc++.h>

using namespace std;

int main() {

  long long number_1, number_2;
  cin >> number_1 >> number_2;

  cout << number_1 << " + " << number_2 << " = " << number_1 + number_2 << endl;
  cout << number_1 << " * " << number_2 << " = " << number_1 * number_2 << endl;
  cout << number_1 << " - " << number_2 << " = " << number_1 - number_2 << endl;

}
